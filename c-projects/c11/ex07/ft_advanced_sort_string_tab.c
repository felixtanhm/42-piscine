void	ft_swap(char **a, char **b)
{
	char	*tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

void	ft_advanced_sort_string_tab(char **tab, int(*cmp)(char *, char *))
{
	int	i;
	int	j;

	i = 0;
	while (tab[i] != 0)
	{
		j = 0;
		while (tab[j] != 0)
		{
			if (tab[j + 1] && cmp(tab[i], tab[j + 1]) > 0)
				ft_swap(&tab[i], &tab[j + 1]);
			j++;
		}
		i++;
	}
}
