int ft_strncmp(char *s1, char *s2, unsigned int n)
{
  unsigned int i = 0;

  while(s1[i] != '\0' && s2[i] != '\0' && i < n-1)
  while(s1[i] != '\0' && s2[i] != '\0' && s1[i] == s2[i] && i < n )
  {
	if(s1[i] != s2[i])
		return (s1[i] - s2[i]);
	i++;
  }

  return (0);
}
